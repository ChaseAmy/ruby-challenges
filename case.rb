weather = 'sunny'

case weather
when 'rainy'
  puts "No need to get dressed. It's a stay in bed day."
when 'cloudy'
  puts "The sun could still come out, wear layers."
when 'snowing'
  puts "Remember to bring some gloves. Snowball fight at lunch!"
when 'sunny'
  puts "It's going to be a beautiful day in the neighborhood!"
when 'windy'
  puts "Lead boots. I don't want you to blow away."
else
  puts "Hmm, well I don't know what to tell you!"
end
